#!/usr/bin/env python3
"""Convert a specification-fidelity ``domain-specification.md`` to ``domain-model.json``.

Usage::

    python md_domain_specification_to_domain_graph.py <input.md> <output.json>

Format expected (generated by abd-domain-specification skill — see
``practices/domain-driven-design/skills/abd-domain-specification/reference/concepts.md``)::

    ---
    state: class-model
    ---

    # Module: [pml-my]

    Scope-or-intro paragraph for the module.

    ---

    # Core Domain

    ## **CustomerKa**

    KA intro paragraph.

    ### **Customer** << Entity >>

    Initialisation: optional one-liner describing how instances are bootstrapped.

    + Customer(email: EmailAddress, password: Password)
    ------
    + << composition >> identity: Identity
    + billing: Billing
        Invariant: billing.id must be present in self-care
    ----
    **Onboarding operations** (metadata.verified = false):

    + searchNumber(keyword: Keyword): List<NumberOption>
        Invariant: keyword is up to 5 characters
        Interaction:
            options = lookup.search(keyword)
            return options

    ### references
    **Ref — Title**
    Source: src/path/to/file.ts
    Locator: <symbol or line range>
    Extract: whole

    ### decisions made

    - decision text
    - another decision

    ---

    # Boundary Domain

    Optional boundary intro paragraph.

    ### **Cognito** << Service >> [Identity Provider — AWS IAM]

    Initialisation: optional initialisation note.
    ------
    ----
    + signUp(email: EmailAddress, password: Password): AccountToken

Behavior:
    - Parses the markdown into a validated ``abd-domain-model/v1`` dict.
    - Lifts all spec-only fields the schema supports (stereotype, stereotype_note,
      initialisation, named parameters on constructor/operations, operation phase,
      module intro, boundary_domain intro, references, decisions).
    - Synthesises a relationship at KA scope for every property whose markdown carried
      a ``<< composition | aggregation | association | produces >>`` stereotype prefix.
      ``<< produces >>`` is normalised to ``association``. Source-end role defaults to
      lowerCamelCase of the owning class name; target-end role is the property name;
      cardinality defaults to ``1..1`` both ends (the modeller refines later).
    - Exits with code 2 when the file does not match the format signature, so the
      caller can fall back to AI generation (mirrors ``md_thin_slice_to_story_graph``).
    - Exits with code 1 on validator rejection — the JSON is written first, then
      validated; a non-zero exit means the markdown needs a fix to match the schema.

Out of scope (deliberately, per P1.2 of the multi-backend plan):
    - Relationship promotion to module scope when both ends span different KAs.
    - Synthesising relationships from plain ``List<X>`` properties without a stereotype.
    - Round-trip back to markdown (that lives in a separate emitter script).
"""
